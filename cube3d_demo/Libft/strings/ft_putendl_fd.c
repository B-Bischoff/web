#include "../libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	c;

	if (s)
	{
		i = 0;
		while (s[i])
		{
			c = s[i];
			write(fd, &c, 1);
			i++;
		}
	}
	c = '\n';
	write(fd, &c, 1);
}
